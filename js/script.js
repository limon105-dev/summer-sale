// function for produt:1--
document.getElementById("product1").addEventListener('click', function () {
    const productName = getTextElementValueById("product-name1");
    setElementInnerText(productName);

    const productPrice = getNumberValueById('product-price1');
    const previousPriceTotal = getNumberValueById('total-price');
    const presentPriceTotal = previousPriceTotal + productPrice;
    setElementValueById('total-price', presentPriceTotal);

    const purchaseBtn = document.getElementById('purchase-btn');
    if (presentPriceTotal > 0) {
        purchaseBtn.removeAttribute('disabled');
    } else {
        purchaseBtn.setAttribute('disabled', true);
    }

    const cuponApplyBtn = document.getElementById('cupon-apply-btn');
    if (presentPriceTotal >= 200) {
        cuponApplyBtn.removeAttribute('disabled');
    } else {
        cuponApplyBtn.setAttribute('disabled', true);
    }

    return presentPriceTotal;
});

